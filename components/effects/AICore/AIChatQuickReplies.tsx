export function AIChatQuickReplies({
  replies,
  onSelect,
  disabled = false,
}: AIChatQuickRepliesProps) {
  const prefersReducedMotion = useReducedMotion();

  const containerVariants = {
    hidden: { opacity: 0 },
    visible: {
      opacity: 1,
      transition: {
        staggerChildren: prefersReducedMotion ? 0 : 0.05,
      },
    },
  };

  const itemVariants = {
    hidden: { opacity: 0, y: 10 },
    visible: { opacity: 1, y: 0 },
  };

  return (
    <motion.div
      className="flex flex-wrap gap-2 pt-2"
      variants={containerVariants}
      initial="hidden"
      animate="visible"
    >
      {replies.map((reply) => (
        <motion.button
          key={reply.id}
          onClick={() => onSelect(reply)}
          disabled={disabled}
          className="inline-flex items-center gap-1.5 rounded-full border border-accent/30 bg-white px-3 py-1.5 text-xs font-medium text-primary shadow-sm transition-all hover:border-accent hover:bg-accent/5 hover:shadow-md focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-accent focus-visible:ring-offset-1 disabled:cursor-not-allowed disabled:opacity-50"
          variants={itemVariants}
          whileHover={prefersReducedMotion ? {} : { scale: 1.02 }}
          whileTap={prefersReducedMotion ? {} : { scale: 0.98 }}
        >
          {reply.icon && icons[reply.icon] && (
            <span className="text-accent">{icons[reply.icon]}</span>
          )}
          <span>{reply.label}</span>
        </motion.button>
      ))}
    </motion.div>
  );
}
